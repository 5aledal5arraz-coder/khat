/**
 * Studio website packages — persistence routed through
 * studio_analysis_records (Khat Brain Phase 5).
 *
 * Note: the studio→episode push route reads `linked_episode_id` from
 * the package and uses it to drive the RPC. We preserve that field by
 * mirroring it into `data.linked_episode_id` AND keeping the public
 * `StudioWebsitePackage.linked_episode_id` accessor stable.
 */

import type {
  StudioWebsitePackage,
  StudioWebsitePackageStatus,
  WebsiteQuoteItem,
  WebsiteResourceItem,
  WebsiteTimestampItem,
} from "@/types/database"
import {
  upsertStudioAnalysisRecord,
  getStudioAnalysisRecord,
  resolveEirIdForSession,
  type StudioAnalysisRecord,
} from "./analysis-records"

interface WebsitePackageData {
  hero_summary?: string | null
  full_summary?: string | null
  takeaways?: string[]
  quotes?: WebsiteQuoteItem[]
  resources?: WebsiteResourceItem[]
  timestamps?: WebsiteTimestampItem[]
  custom_title?: string | null
  selected_quote_indices?: number[] | null
  selected_takeaway_indices?: number[] | null
  linked_episode_id?: string | null
  guest_package?: {
    guest_name: string
    guest_bio: string
    guest_photo_url: string | null
    guest_external_links: Record<string, string>
  } | null
}

function mapToLegacyShape(r: StudioAnalysisRecord): StudioWebsitePackage {
  const data = (r.data ?? {}) as WebsitePackageData
  return {
    id: r.id,
    session_id: r.studio_session_id ?? "",
    status: r.status as StudioWebsitePackageStatus,
    hero_summary: data.hero_summary ?? null,
    full_summary: data.full_summary ?? null,
    takeaways: data.takeaways ?? [],
    quotes: data.quotes ?? [],
    resources: data.resources ?? [],
    timestamps: data.timestamps ?? [],
    custom_title: data.custom_title ?? null,
    selected_quote_indices: data.selected_quote_indices ?? null,
    selected_takeaway_indices: data.selected_takeaway_indices ?? null,
    linked_episode_id: data.linked_episode_id ?? null,
    guest_package: data.guest_package ?? null,
    raw_openai_response: r.raw_provider_response,
    error_message: r.error,
    created_at: r.created_at,
    updated_at: r.updated_at,
  } as unknown as StudioWebsitePackage
}

export async function getWebsitePackageForSession(
  sessionId: string,
): Promise<StudioWebsitePackage | null> {
  try {
    const r = await getStudioAnalysisRecord(sessionId, "website_package")
    return r ? mapToLegacyShape(r) : null
  } catch (err) {
    console.error("Error fetching website package:", err)
    return null
  }
}

export async function createWebsitePackage(
  sessionId: string,
  entry: {
    status: StudioWebsitePackageStatus
    hero_summary: string | null
    full_summary: string | null
    takeaways: string[]
    quotes: WebsiteQuoteItem[]
    resources: WebsiteResourceItem[]
    timestamps: WebsiteTimestampItem[]
    custom_title?: string | null
    selected_quote_indices?: number[] | null
    selected_takeaway_indices?: number[] | null
    linked_episode_id: string | null
    guest_package?: WebsitePackageData["guest_package"]
    raw_openai_response: Record<string, unknown> | null
    error_message: string | null
  },
): Promise<{ success: boolean; data?: StudioWebsitePackage; error?: string }> {
  try {
    const eirId = await resolveEirIdForSession(sessionId)
    const data: WebsitePackageData = {
      hero_summary: entry.hero_summary,
      full_summary: entry.full_summary,
      takeaways: entry.takeaways,
      quotes: entry.quotes,
      resources: entry.resources,
      timestamps: entry.timestamps,
      custom_title: entry.custom_title ?? null,
      selected_quote_indices: entry.selected_quote_indices ?? null,
      selected_takeaway_indices: entry.selected_takeaway_indices ?? null,
      linked_episode_id: entry.linked_episode_id,
      guest_package: entry.guest_package ?? null,
    }
    const saved = await upsertStudioAnalysisRecord({
      studio_session_id: sessionId,
      eir_id: eirId,
      kind: "website_package",
      status: entry.status,
      data: data as Record<string, unknown>,
      raw_provider_response: entry.raw_openai_response,
      error: entry.error_message,
      generated_at: new Date(),
    })
    return { success: true, data: mapToLegacyShape(saved) }
  } catch (err: unknown) {
    const message = err instanceof Error ? err.message : String(err)
    return { success: false, error: message }
  }
}

export async function updateWebsitePackage(
  id: string,
  updates: Partial<
    Pick<
      StudioWebsitePackage,
      | "hero_summary"
      | "full_summary"
      | "takeaways"
      | "quotes"
      | "resources"
      | "timestamps"
      | "custom_title"
      | "selected_quote_indices"
      | "selected_takeaway_indices"
      | "linked_episode_id"
      | "guest_package"
    >
  >,
): Promise<{ success: boolean; data?: StudioWebsitePackage; error?: string }> {
  try {
    const { db } = await import("@/lib/db")
    const { studioAnalysisRecords } = await import("@/lib/db/schema/studio-analysis")
    const { eq } = await import("drizzle-orm")

    const existing = await db!
      .select()
      .from(studioAnalysisRecords)
      .where(eq(studioAnalysisRecords.id, id))
      .limit(1)
    if (!existing[0]) return { success: false, error: "Not found" }
    const sessionId = existing[0].studio_session_id
    if (!sessionId) return { success: false, error: "Record has no session" }

    const data = (existing[0].data ?? {}) as WebsitePackageData
    for (const [k, v] of Object.entries(updates) as [keyof WebsitePackageData, unknown][]) {
      if (v !== undefined) (data as Record<string, unknown>)[k as string] = v
    }

    const saved = await upsertStudioAnalysisRecord({
      studio_session_id: sessionId,
      eir_id: existing[0].eir_id,
      kind: "website_package",
      status: existing[0].status as StudioWebsitePackageStatus,
      data: data as Record<string, unknown>,
      raw_provider_response:
        (existing[0].raw_provider_response ?? null) as Record<string, unknown> | null,
      error: existing[0].error,
    })
    return { success: true, data: mapToLegacyShape(saved) }
  } catch (err: unknown) {
    const message = err instanceof Error ? err.message : String(err)
    return { success: false, error: message }
  }
}
